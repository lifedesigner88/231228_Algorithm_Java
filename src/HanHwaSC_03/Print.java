package HanHwaSC_03;

class Print {
    static void P(Object x) {
        System.out.println(x);
    }

    static void P(char[] x) {
        System.out.println(new String(x));
    }

    static void print(Object x) {
        System.out.println(x);
    }

    static void print() {
        System.out.println();
    }

    static void print(char[] x) {
        System.out.println(new String(x));
    }
}
